USE TEST
GO
IF EXISTS(SELECT 1 FROM SYSOBJECTS WHERE NAME='ELEMENTPARAM')
    DROP TABLE ELEMENTPARAM
GO
    CREATE TABLE ELEMENTPARAM
    (SERIAL_NO INTEGER NOT NULL IDENTITY PRIMARY KEY,
     PAGE_ID INTEGER,                             --页面ID
     PAGE_NAME NVARCHAR(20),                      --页面名称
     ELEMENT_NAME NVARCHAR(20),                   --元素名称
     ELEMENT_SELECT NVARCHAR(10),                 --定位元素的方法
     ELEMENT_SELECT_KEY NVARCHAR(20),             --定位元素所需参数
     ELEMENT_ACTION INTEGER,                      --元素操作 1:输入值 2:单击 3.下拉选项
     ELEMENT_ACTION_KEY NVARCHAR(30)              --元素操作所需参数
    )
    CREATE INDEX INDEX_ELEMENTPARAM ON ELEMENTPARAM(PAGE_ID,ELEMENT_NAME)
GO
USE TEST
IF NOT EXISTS(SELECT 1 FROM SYSOBJECTS WHERE NAME='DICTPARAM')BEGIN
    CREATE TABLE DICTPARAM
    (SERIAL_NO INTEGER NOT NULL IDENTITY PRIMARY KEY,
     FUNCTION_ID INTEGER,                             --页面ID或者功能ID
     FUNCTION_NAME NVARCHAR(20),                      --页面名称或者功能名称
     FUNCITON_ELEMENT_NAME NVARCHAR(20),              --元素名称
     FUNCITON_ELEMENT_SELECT NVARCHAR(10),            --定位元素的方法
     FUNCITON_ELEMENT_SELECT_KEY NVARCHAR(20),        --定位元素所需参数
     FUNCITON_ELEMENT_ACTION INTEGER,                 --元素操作 1:输入值 2:单击
     FUNCITON_ELEMENT_ACTION_KEY NVARCHAR(30)         --元素操作所需参数
    )
    CREATE INDEX INDEX_DICTPARAM ON DICTPARAM(FUNCTION_ID,FUNCITON_ELEMENT_NAME)
END